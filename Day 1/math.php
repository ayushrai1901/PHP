<?php
//PHP has a set of math functions that allows you to perform mathematical tasks on numbers.

//PHP pi() function returns the value of PI

echo(pi());

// min()  & max() functions returns the lowest & highest value from a list of arguments
echo(min(0,150,300,20,-8,-200));
echo(max(0,150,30,-8,-200));

//PHP abs() function
//the abs() function returns the absolute (positive) value of a number
echo(abs(-6.7)); 

//PHP sqrt() function
echo(sqrt(64));

//PHP round() function
echo(round(0.40));

//Random Number  
//rand() function generates a random number
echo(rand());

echo(rand(10,100));

?>